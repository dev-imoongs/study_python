# 한 줄 주석(Ctrl + /)
# 설명 글 작성을 위해 사용한다.
# 지금 당장 사용하지 않는 코드에 사용한다.

"""
    범위 주석
"""

# 출력 함수
# 오류를 구체화할 때 사용
print("한동석")

print("한동석", end="♥")
print("20살")
print("한동석", "20살", sep=", ")

# 제어 문자
# 반드시 따옴표 안에서 작성
print("한동석\n20살")
print("이름: \t한동석")
print("나이: \t20살")
print("\"한동석\"", "\'20살\'", sep=", ")
